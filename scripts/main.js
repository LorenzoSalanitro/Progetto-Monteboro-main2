const LANGUAGE_STORAGE_KEY = "monteboro-language";

function getStoredLanguage() {
  try {
    const storedLang = localStorage.getItem(LANGUAGE_STORAGE_KEY);
    return storedLang && translations[storedLang] ? storedLang : "it";
  } catch (error) {
    return "it";
  }
}

function applyLanguage(lang) {
  const normalizedLang = translations[lang] ? lang : "it";
  const elements = document.querySelectorAll("[data-i18n]");
  elements.forEach((element) => {
    const key = element.getAttribute("data-i18n");
    if (translations[normalizedLang] && translations[normalizedLang][key]) {
      element.innerHTML = translations[normalizedLang][key];
    }
  });
  document.documentElement.lang = normalizedLang;
  setLanguageButtonLabel(normalizedLang);

  try {
    localStorage.setItem(LANGUAGE_STORAGE_KEY, normalizedLang);
  } catch (error) {
    // Ignore storage errors
  }
}

function setLanguageButtonLabel(lang) {
  const langToggle = document.querySelector(".lang-toggle");
  const labels = { it: "IT", en: "EN", de: "DE" };
  if (langToggle) {
    langToggle.textContent = labels[lang] || "Lingua";
  }
}

function closeLanguageMenu() {
  const langMenu = document.querySelector(".lang-menu");
  const langToggle = document.querySelector(".lang-toggle");
  if (langMenu) langMenu.classList.remove("open");
  if (langToggle) langToggle.setAttribute("aria-expanded", "false");
}

/**
 * Funzioni per la gestione del carrello (localStorage)
 */
function getCart() {
  try {
    const cart = localStorage.getItem('shoppingCart');
    return cart ? JSON.parse(cart) : [];
  } catch (e) {
    return [];
  }
}

function saveCart(cart) {
  try {
    localStorage.setItem('shoppingCart', JSON.stringify(cart));
  } catch (e) {}
}

function addToCart(item) {
  const cart = getCart();
  const existingItemIndex = cart.findIndex(cartItem => cartItem.id === item.id);
  if (existingItemIndex > -1) {
    cart[existingItemIndex].quantity += item.quantity;
  } else {
    cart.push(item);
  }
  saveCart(cart);
}

/**
 * Renderizza gli articoli nella pagina del carrello.
 */
function renderCartPage() {
  const container = document.getElementById('cart-items-container');
  if (!container) return;
  const cart = getCart();
  const emptyMsg = document.getElementById('empty-cart-message');
  const summary = document.getElementById('cart-summary');
  const totalItemsSpan = document.getElementById('cart-total-items');
  container.innerHTML = '';
  if (cart.length === 0) {
    if (emptyMsg) emptyMsg.style.display = 'block';
    if (summary) summary.style.display = 'none';
  } else {
    if (emptyMsg) emptyMsg.style.display = 'none';
    const ul = document.createElement('ul');
    ul.style.listStyle = 'none';
    ul.style.padding = '0';
    cart.forEach(item => {
      const li = document.createElement('li');
      li.style.cssText = 'display: flex; justify-content: space-between; padding: 1rem 0; border-bottom: 1px solid var(--border);';
      li.innerHTML = `<span>${item.name}</span><strong>x${item.quantity}</strong>`;
      ul.appendChild(li);
    });
    container.appendChild(ul);
    if (totalItemsSpan) totalItemsSpan.textContent = cart.reduce((s, i) => s + i.quantity, 0);
    if (summary) summary.style.display = 'block';
  }
}

